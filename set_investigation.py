# use {} to create a set
list1 = {"red", "green", "blue", "yellow", "red"}
list2 = {"black", "white", "red", "yellow", "orange"}

# set operations are supported
print (list1.union(list2))
print (list1.intersection(list2))
print (list1.difference(list2))
print (list2.difference(list1))










